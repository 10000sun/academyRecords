package sec03.exam02;

public class Product {
	private int pno;
	private String name;
	private int price;
	private int stock;
	
	
	public int getPno() {
		return pno;
	}
	public String getName() {
		return name;
	}
	public int getPrice() {
		return price;
	}
	public int getStock() {
		return stock;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public void setStock(int stock) {
		this.stock = stock;
	}
	
	
}
