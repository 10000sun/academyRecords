import turtle
import random

def screenRightClick(x, y) :
    global r, g, b
    sSize = random.randrange(1, 10)
    turtle.pencolor(random())
    turtle.pendown()
    turtle.goto(x, y)
    turtle.stamp(random())
    turtle.stampsize(sSize)

    turtle.title
    turtle.shape('turtle')
    turtle.pensize(10)

    turtleonscreenclick(screenRightClick, 3)
    turtle.done()
#변수 선언
r, g, b= 0.0, 0.0, 0.0
